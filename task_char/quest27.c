  /* Написать программу, которая переводила бы строку на английском языке в морзянку. */

#include <stdio.h>

int main() {
  char str[100];
  fgets(str, 100, stdin);

  char *morse[36] = {".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--..","-----",".----","..---","...--","....-",".....","-....","--...","---..","----."};

  for (int i=0; i<str[i]; i++) {
    if (str[i]>=65 && str[i]<=90) str[i]+=32;
    if (str[i]==' ') {
      printf("|:_..._:|");
      continue;
    }
    if (str[i]%97>=0 && str[i]%97<=25) {
      printf("%s", morse[str[i]%97]);
    } else if (str[i]%97>47 && str[i]%97<58) {
      printf("%s", morse[str[i]%48+26]);
    }
    
    printf("|");
  }

  return 0;
}

